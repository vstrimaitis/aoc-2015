from collections import *
from typing import *

def calc_neigh_sum(grid: List[List[int]], i: int, j: int) -> int:
    ans = 0
    for di in [-1, 0, 1]:
        for dj in [-1, 0, 1]:
            if di == 0 and dj == 0:
                continue
            ii = i + di
            jj = j + dj
            if 0 <= ii < len(grid) and 0 <= jj < len(grid[0]):
                ans += grid[ii][jj]
    return ans

def simulate(grid: List[List[int]], corners_fixed: bool) -> List[List[int]]:
    n = len(grid)
    m = len(grid[0])
    new_grid = [[grid[i][j] for j in range(m)] for i in range(n)]
    corners = {(0, 0), (n-1, 0), (0, m-1), (n-1, m-1)}
    if corners_fixed:
        for i, j in corners:
            grid[i][j] = 1
    for i in range(n):
        for j in range(m):
            if corners_fixed and (i, j) in corners:
                new_grid[i][j] = 1
                continue
            neigh_sum = calc_neigh_sum(grid, i, j)
            if grid[i][j] == 1:
                if neigh_sum in {2, 3}:
                    new_grid[i][j] = 1
                else:
                    new_grid[i][j] = 0
            else:
                if neigh_sum == 3:
                    new_grid[i][j] = 1
                else:
                    new_grid[i][j] = 0
    return new_grid


def display(grid):
    for r in grid:
        for c in r:
            print("#" if c else " ", end="")
        print()
    print()

def _solve(data: str, steps: int, corners_fixed: bool = False) -> int:
    grid = [[1 if c == "#" else 0 for c in r] for r in data.split("\n")]
    for _ in range(steps):
        grid = simulate(grid, corners_fixed)
    return sum(sum(grid, []))

def solve(data: str) -> Tuple[str, str]:
    ans1 = _solve(data, 100)
    ans2 = _solve(data, 100, True)
    return str(ans1), str(ans2)

